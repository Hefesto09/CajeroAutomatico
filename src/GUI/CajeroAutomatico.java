    private void btn_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aceptarActionPerformed
        cuentaIngresada = txt_clave.getText();
        if(txt_clave.isEnabled()){
            if (cuentas.containsKey(cuentaIngresada)) {
                Persona persona = cuentas.get(cuentaIngresada);
                txt_usuario.setText(persona.getDatosPersonales());
                txt_titulo.setText("SELECCIONE UNA OPCIÓN");
                ingresoClaveOF();
                btnOpcionesON();
                btnNmrOF();
                btnModificarOF();
                usuario = cuentaIngresada;
            } else {
                intentos--;
                if (intentos > 0) {
                    txt_titulo.setText("Cuenta incorrecta. Intentos restantes" + intentos);
                    txt_clave.setText("");
                } else {
                    txt_titulo.setText("Ha agotado los intentos. Gracias");
                    btn_ingresar.setEnabled(true);
                    btnNmrOF();
                    btnModificarOF();
                    panel_clave.setVisible(false);
                }
            }
        }
        else{ 
            
            if(cuentas.containsKey(usuario)){
                Persona persona = cuentas.get(usuario);
                deposito = Double.parseDouble(txt_opciones.getText());
                persona.setSaldo(persona.getSaldo() + deposito);
                txt_titulo.setText("DEPOSITO REALIZADO");
                txt_opciones.setText("");

                    }

            }
        
    }//GEN-LAST:event_btn_aceptarActionPerformed

