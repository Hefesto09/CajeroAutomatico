    public void agregarNumero(String numero, JLabel etiqueta) {
    if (etiqueta.getText().equals(" ")) {
        etiqueta.setText(numero);
        } 
    else {
        String nuevoNumero = etiqueta.getText() + numero;
        etiqueta.setText(nuevoNumero);
        }
    }
    
    public void borrarUltimoCaracter(JLabel etiqueta) {
    String textoActual = etiqueta.getText();
    if (!textoActual.isEmpty()) {
        etiqueta.setText(textoActual.substring(0, textoActual.length() - 1));
        }
    }
    
